    <h2><strong>Trochu z našej histórie:</strong></h2><hr />

    <p>V začiatkoch sme boli malá firma propagujúca cvičenie z pohľadu pre človeka nie tak známeho a to ako napríklad <br />
        gaučing alebo ležing. Tieto typy športov boli u zákazníkov obľúbené, avšak na významný chpodujatiach ako olympiáda, <br />
        alebo majstrovstvá sveta bohužiaľ nemali potenciál. Preto sme sa v roku 1932 rozhodli že prejdeme na iné športy, <br />
        ako sú napríklad zdvíhanie činiek, či hranie futbalu, alebo hokeja. Samozrejme vtedy ešte bez chráničov na hlavu, <br />
        no podstatou bolo, že sme mali chránené ostatné časti tela.<br />
        Časom sme zistili, že je to výnosné a preto sme si zadovážili vlastné pozemky na ktorých sme vybudovali vlastné<br />
        štadióny, haly, dráhy, či cvičiská tak, ako ich poznáme v súčasnosti.</p><br />

    <h2><strong>Súčasnosť i budúcnosť:</strong></h2><hr />
    <h4>Súčasnosť: </h4>
    <p>V súčasnosti máme plne rozbehnutú rodinnú firmu, ktorú založil náš ded, ktorý nešťastne padol v boji... a vyvrtol <br />
        si nohu a tak žil ešte mnoho ďalších rokov šťastne u seba doma, kde sa nudil tak, že vytvoril tento plán. <br />
        V súčasnosti vlastníme 6 futbalových ihrísk, 3 hokejové haly, 5 hokejbalových alebo futsalových hál, 2 golfové <br />
        ihriská, a 2 atletické haly.</p><br />

    <h4>Budúcnosť:</h4>
    <p>Do budúcnosti by sme ešte chceli vytvoriť viac komfortu pre našich zákazníkov a preto sa budeme snažiť zlepšiť naše <br />
        služby, priestory a vlastne v podstate všetko. Ďalej by sme sa chceli opäť pokúsiť dostať do popredia športy ako <br />
        sú gaučing, ležing, alebo závodné žranie (pozn. neodvážime sa to nazvať jedením). Ak sa nám raz podarí preraziť <br />
        vo svete s týmito nezvyčajnými disciplínami, vďaka bude patriť len a len vám, našim klientom.</p>

